package edu.learning;

public class RelationalOperators {

	public static void main(String[] args) {

		int myFirstNum =55;
		int mySecondNum =55;
		
		if(myFirstNum <= mySecondNum)
		System.out.println(myFirstNum + "<=" + mySecondNum);
		
		else if(myFirstNum >= mySecondNum)
			System.out.println(myFirstNum + ">=" + mySecondNum);
		
		else if (myFirstNum == mySecondNum)
			System.out.println(myFirstNum + "==" + mySecondNum);

	}

}
